#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	a;

	a = 0;
	if (s != NULL)
		while (s[a])
			write (1, &s[a++], 1);
	else
	{
		ft_putstr("(null)");
		return (6);
	}
	return (a);
}
